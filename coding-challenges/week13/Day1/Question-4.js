/*Question - 4 

Create a function that takes a number as an argument, increments the number by +1 and returns the result.
Examples
addition(0) ➞ 1

addition(9) ➞ 10

addition(-3) ➞ -2
*/

addition=(num)=>{return ++num}
console.log(addition(5))     //Output: 6
console.log(addition(-3))    //Output: -2
